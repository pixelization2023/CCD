namespace CCDInspection.Core.Enums
{
    /// <summary>
    /// 机台状态(三色灯)
    /// </summary>
    public enum MachineStatus
    {
        /// <summary>
        /// 软件打开未初始化：机台刚启动，软件界面已打开但尚未完成初始化。此时所有设备处于初始状态，等待用户操作或自动流程启动。此状态对应红灯，提示用户需要进行初始化操作。
        /// </summary>
        SoftwareOpen,  // 0 软件打开未初始化 → 红灯
        /// <summary>
        /// 初始化中：正在执行初始化流程，设备逐步完成自检、参数加载、设备复位等操作。此时机台处于准备状态，但尚未完全就绪。此状态对应黄灯，提示用户正在进行初始化操作，请稍候。
        /// </summary>
        Initial,       // 1 初始化中 → 黄灯
        /// <summary>
        /// 初始化完成：所有设备已完成初始化，机台处于就绪状态，等待启动信号。此时用户可以开始运行检测流程。此状态对应绿灯，提示用户机台已准备就绪，可以开始操作。
        /// </summary>
        Initialed,     // 2 初始化完成 → 绿灯
        /// <summary>
        /// 运行中：正在执行自动检测流程，设备根据流程步骤自动动作。此时机台处于工作状态，用户可以监控检测过程但不建议进行干预。此状态对应绿灯，提示用户机台正在正常运行。
        /// </summary>
        Start,         // 3 运行中 → 绿灯
        /// <summary>
        /// 停止：自动检测流程暂停或已停止，设备保持当前状态不动，等待继续或停止指令。此时机台处于非工作状态，用户可以进行调整或维护操作。此状态对应黄灯，提示用户机台已停止，请进行必要的操作后再继续运行。
        /// </summary>
        Stop,          // 4 停止
        /// <summary>
        /// 报警：发生异常需要人工干预，所有设备急停，等待复位指令。此时机台处于异常状态，用户需要检查报警原因并进行处理。此状态对应红灯和蜂鸣器，提示用户机台发生异常，请立即检查并处理问题。
        /// </summary>
        Alarm          // 5 报警 → 红灯+蜂鸣
    }
}
